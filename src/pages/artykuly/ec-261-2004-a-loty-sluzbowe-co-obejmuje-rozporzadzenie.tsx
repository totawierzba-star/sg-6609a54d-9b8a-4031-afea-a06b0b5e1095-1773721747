<section class="article-section">
  <h2>Wyjątki i nadzwyczajne okoliczności</h2>
  <p>Rozporządzenie WE 261/2004 przewiduje sytuacje, w których linie lotnicze mogą zwolnić się z obowiązku wypłaty odszkodowania. Najważniejszym wyjątkiem są nadzwyczajne okoliczności, których nie można było uniknąć nawet przy zachowaniu wszelkich środków ostrożności. W takich przypadkach linia nie musi wypłacać odszkodowania, ale wciąż ma obowiązek zapewnienia opieki pasażerom.</p>
  
  <p>Do nadzwyczajnych okoliczności zalicza się m.in.:
  - Niestabilność polityczna w kraju docelowym
  - Warunki meteorologiczne niebezpieczne dla lotu
  - Ryzyko bezpieczeństwa lotniczego
  - Nieoczekiwane defekty samolotu ujawnione podczas operacji lotniczych
  - Strajki personelu lotniczego lub kontroli lotów
  - Ograniczenia w ruchu lotniczym</p>
  
  <div class="callout callout--info">
    <span class="callout__icon">ℹ</span>
    <div class="callout__content">
      <strong>Ważne:</strong> Problemy techniczne wynikające z utrzymania samolotu nie są uważane za nadzwyczajne okoliczności, chyba że zostały zidentyfikowane przez producenta lub odpowiednie organy.
    </div>
  </div>
  
  <p>Inne wyjątki to sytuacje, gdy pasażer nie stawił się na odprawie w wymaganym czasie, nie posiada ważnych dokumentów podróży, lub gdy bilet jest nieważny. W takich przypadkach odmowa przyjęcia na pokład jest uzasadniona i nie uprawnia do odszkodowania.</p>
</section>

